


class GameScore {

	public static void main(String[] args) {

		int points = 820 ;

		if( points >= 700 ) {
			System.out.println("Awesome ! Outstanding Score !!");
		} else if( points >= 500 ) {
			System.out.println("Great ! Excellent Score !!");
		} else if( points >= 250 ) {
			System.out.println("Average Score !!");
		} else {
			System.out.println("Better Luck Next Time !!");
		}

	}

}

