


class Divisible {

	public static void main(String[] args) {

		int x = 210 ;

		if( x % 7 == 0 ){
			System.out.println( x + " is Divisible by 7 .");
		} else if( x % 7 != 0 ){
			System.out.println( x + " is not Divisible by 7 .");
		} else {
			System.out.println("Invalid Data. ");
		}

	}

}

