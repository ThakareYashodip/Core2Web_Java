


class SwitchDemo21 {

        public static void main(String[] args) {

                 boolean x = true ;

                System.out.println("Before Switch");

                switch(x){
                        case true:
                                System.out.println("True");
                                break;
                        case false:
                                System.out.println("False");
                                break;
                        default :
                                System.out.println("In default state");
                }

        }

}
