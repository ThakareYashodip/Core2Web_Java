
import java.util.*;

class Yash6 {

	public static void main(String[] args) {

		Scanner ganu = new Scanner(System.in);
		System.out.print(" Enter your Num1 : ");
		int num1 = ganu.nextInt();

		System.out.print(" Enter your Num2 : ");
		int num2 = ganu.nextInt();

		for( int i = num1 ; i <= num2 ; i++ ) {
			System.out.print( i + " " );
		}
		System.out.println();
	}

}

